
function bgm() {
    const song = document.getElementById("song");

    if (song.paused) {
        song.volume = 0.6;
        song.play().catch(err => {
            console.log("Audio play error:", err);
        });
    } else {
        song.pause();
        song.currentTime = 0; // optional: restart from beginning
    }
}

